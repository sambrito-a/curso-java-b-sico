public class Incremento {
    public static void main(String[] args) {
        //x repetição
        int numero = 5;
        numero = numero + 2;
        System.out.println(numero); // 7

        //Incremendo
        int numero2 = 5;
        numero2++; // incremento
        System.out.println(numero2); // 6

        //Outro metodo de incremento
        int numero3 = 5;
        System.out.println(++numero3); // 6

        //Metodo de negação em memoria, não altera o valor da variavel
        boolean variavel = true;
        System.out.println(!variavel); // false

        //Negando o valor da variavel
        boolean variavel2 =  false;
        variavel2 = !variavel2; 
        System.out.println(variavel2); 

}
 
}
