public class OperadoresUnarios {
    public static void main(String[] args) {
        int numero = 5; // 5 é um número positivo
        System.out.println(-numero); // Saída: -5, o operador unário - inverte o sinal do número
        System.out.println(numero); // Saída: 5, o número permanece o mesmo
        numero = - numero; // O número agora é negativo
        System.out.println(numero); // Saída: -5, o número agora é negativo
        numero = numero * -1; // Forma de pegar o valor negativo e torná-lo positivo novamente
        // O número agora é positivo novamente
        System.out.println(numero); 
    }
}
 